/**
 * Categorize coupons for different landing page sections
 */
export function categorizeCouponsForLanding(coupons: Coupon[]) {
  // Filter out expired coupons for main sections
  const activeCoupons = coupons.filter((c) => !c.isExpired)

  // Hero coupons - featured coupons for carousel (top 10)
  const heroCoupons = activeCoupons.filter((c) => c.featured).slice(0, 10)

  // Featured coupons - high-value deals (discount >= 30%, top 6)
  const featuredCoupons = activeCoupons
    .filter((c) => c.featured && c.discount >= 30)
    .slice(0, 6)

  // Expiring coupons - expiring within 7 days
  const expiringCoupons = getExpiringCoupons(activeCoupons, 8)

  // Grid coupons - general coupons for bottom grid (first 12)
  const gridCoupons = activeCoupons.slice(0, 12)

  // Trending coupons - most popular (top 10)
  const trendingCoupons = getTrendingCoupons(activeCoupons, 10)

  return {
    heroCoupons,
    featuredCoupons,
    expiringCoupons,
    gridCoupons,
    trendingCoupons,
  }
}
